export function roundRgb(rgb: number) {
    return Math.round(rgb * 100) / 100;
}
export function colorToKey(color: IColor): string {
    return `${roundRgb(color.A)}:${roundRgb(color.R)}:${roundRgb(color.G)}:${roundRgb(color.B)}`;
}
